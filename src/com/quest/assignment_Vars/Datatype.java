package com.quest.assignment_Vars;

public class Datatype {
    public static void main(String[] args) {
        //byte
        byte myNumber=120;
        System.out.println(myNumber);
        // short
        Short myNumber1=12000;
        System.out.println(myNumber1);
        // integer
        Integer myNumber2=9876543;
        System.out.println(myNumber2);
        // long
        Long myNumber3=98765432L;
        System.out.println(myNumber3);
        //float
        Float myNumber4=1.25f;
        System.out.println(myNumber4);
        //double
        Double myNumber5=3.763845d;
        System.out.println(myNumber5);
        //boolean
        Boolean learnJava=true;
        System.out.println(learnJava);
        //char
        char myChar='A';
        System.out.println(myChar);
        //string
        String myString="Hello World";
        System.out.println(myString);

    }
}
