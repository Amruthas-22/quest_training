package com.quest.assignment_Vars;

public class DoWhile {
    public static void main(String[] args) {
        int i = 1;  // Initialize the variable 'i' to 1

        do {  // Start of the do-while loop
            System.out.println(i);  // Print the current value of 'i'
            ++i;  // Increment the value of 'i' by 1
        }
        while(i < 5);  // Continue the loop as long as 'i' is less than 5

    }
}

